import { writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const root = join(__dirname, "..");
const iso = new Date().toISOString();
const out = `// Auto-generated by scripts/write-build-info.mjs — do not edit
export const BUILD_TIME_ISO: string = ${JSON.stringify(iso)};
`;

const target = join(root, "src/lib/buildInfo.generated.ts");
writeFileSync(target, out, "utf8");
console.log("[build-info] wrote", iso);
