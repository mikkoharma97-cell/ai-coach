/**
 * Kirjoittaa src/lib/buildInfo.generated.ts ennen next build — yksi totuus build-ajasta ja versiosta.
 */
import { randomBytes } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const root = join(__dirname, "..");
const pkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const stamp = new Date();
const iso = stamp.toISOString();
const ver = typeof pkg.version === "string" ? pkg.version : "0.0.0";
/** Sama hetki kuin BUILD_TIME_ISO — näkyvä date/time (fi) ilman manuaalikorjauksia. */
const dateFi = stamp.toLocaleDateString("fi-FI", {
  day: "numeric",
  month: "numeric",
  year: "numeric",
});
const timeFi = stamp.toLocaleTimeString("fi-FI", {
  hour: "2-digit",
  minute: "2-digit",
  second: "2-digit",
  hour12: false,
});
/** Yksi rivi build-markerille — sama merkkijono SSR:ssä ja clientissä (ei render-aikaa). */
const displayLine = `${dateFi} · ${timeFi}`;
/** Unique per write — deploy / npm run build fingerprint. */
const buildId = randomBytes(3).toString("hex");
const out = `// Auto-generated by scripts/write-build-info.mjs — do not edit
export const BUILD_TIME_ISO: string = ${JSON.stringify(iso)};
export const APP_VERSION: string = ${JSON.stringify(ver)};
export const BUILD_ID: string = ${JSON.stringify(buildId)};
export const BUILD_DATE_DISPLAY: string = ${JSON.stringify(dateFi)};
export const BUILD_TIME_DISPLAY: string = ${JSON.stringify(timeFi)};
export const BUILD_DISPLAY_LINE: string = ${JSON.stringify(displayLine)};
`;

const target = join(root, "src/lib/buildInfo.generated.ts");
writeFileSync(target, out, "utf8");
console.log("[build-info] wrote", iso, "app", ver);
