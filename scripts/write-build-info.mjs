/**
 * Kirjoittaa src/lib/buildInfo.generated.ts ennen next build — yksi totuus build-ajasta ja versiosta.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const root = join(__dirname, "..");
const pkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const iso = new Date().toISOString();
const ver = typeof pkg.version === "string" ? pkg.version : "0.0.0";
const out = `// Auto-generated by scripts/write-build-info.mjs — do not edit
export const BUILD_TIME_ISO: string = ${JSON.stringify(iso)};
export const APP_VERSION: string = ${JSON.stringify(ver)};
`;

const target = join(root, "src/lib/buildInfo.generated.ts");
writeFileSync(target, out, "utf8");
console.log("[build-info] wrote", iso, "app", ver);
